public class increment_decrement {
    public static void main(String[] args){
        int i = 50;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        int j = 90;
        System.out.println(--j);
        System.out.println(j);
        System.out.println(j--);
        //for character
        char ch = 'a';
        System.out.println(++ch);

    }
}
